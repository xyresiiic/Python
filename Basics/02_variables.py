# This program demonstrates basic variables and data types in Python

# Integer variable
age = 25
print(f"Age: {age} (Type: {type(age)})")

# Float variable
height = 5.9
print(f"Height: {height} (Type: {type(height)})")

# String variable
name = "Alice"
print(f"Name: {name} (Type: {type(name)})")

# Boolean variable
is_student = True
print(f"Is Student: {is_student} (Type: {type(is_student)})")

# Basic math with variables
years_until_30 = 30 - age
print(f"In {years_until_30} years, {name} will be 30 years old.")
